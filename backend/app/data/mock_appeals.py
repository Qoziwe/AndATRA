def generate_mock_appeals(district_count: int = 8) -> list[dict]:
    """Generate 50+ mock appeals with realistic data spread over last 30 days.

    Args:
        district_count: Number of districts in the DB.

    Returns:
        List of dicts with appeal data.
    """
    random.seed(42)  # Deterministic for reproducibility
    now = datetime.now(timezone.utc)
    appeals = []

    for i, text in enumerate(APPEAL_TEXTS):
        days_ago = random.randint(0, 30)
        hours_ago = random.randint(0, 23)
        created = now - timedelta(days=days_ago, hours=hours_ago)

        category_slug = APPEAL_CATEGORY_MAP.get(i, random.choice(CATEGORY_SLUGS))
        status = random.choices(STATUSES, weights=STATUS_WEIGHTS, k=1)[0]
        priority = random.choices(PRIORITIES, weights=PRIORITY_WEIGHTS, k=1)[0]

        # Random district (1-indexed)
        district_id = random.randint(1, district_count)

        # Random coordinates around Almaty center
